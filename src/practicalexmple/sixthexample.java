package practicalexmple;

public class sixthexample {
//program for printing pyramid pattern in java
	public static void main(String[] args) {
		
		System.out.println("*");
		
		for(int i=1; i<5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.println("*");
				System.out.println("\t");
			}
			System.out.println();
		}

	}

}
